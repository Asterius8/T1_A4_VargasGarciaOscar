public class Calculadora {

	public double realizarSuma(double n1, double n2) {
		
		return n1 + n2;
		
	}
	
	public double realizarResta(double n1, double n2) {
		
		return n1 - n2;
		
	}
	
	public double realizarMultiplicacion(double n1, double n2) {
		
		return n1 * n2;
		
	}
	
	public double realizarDivision(double n1, double n2) {
		
		return n1 / n2;
		
	}
	
	public double numero_Negativo(double n) {
		
		return -1 * n;
		
	}
	
	public double realizarRaiz(double n) {
		
		return Math.sqrt(n);
		
	}
	
}
